package JavaOperators;

public class logicalOperator {
    public static void main(String[] args) {
        boolean x = true ;
        boolean y = false ;
        System.out.println("The logical AND return true when both condition are true " + "x && y :  " +( x && y));
        System.out.println("The logical OR return True if at least 1 condition is True " + "x || y :  " + (x || y));
        System.out.println("The logical NOT operator used to vise - versa the Condition " + "!x :  " + ( !x ) +  "  !y : " + (!y ));
    }
}
