package JavaOperators;

public class AssignmentOperator {
    public static void main(String[] args) {
       int h = 11 ;
        System.out.println("adding left operand with Right Operand   " + " h += 3  =  " + (h += 3));
        System.out.println("subtracting right operand from left Operand   " + " h -= 4 =  " + (h -= 4));
        System.out.println("Multiplying left operand with right operand   " + " h *= 2 =  "+ (h *= 2));
        System.out.println("Dividing left operand by right operand  " + " h /= 2 =  " + (h/= 2));
        System.out.println("Modulo of left operand by right operand" + " h %= 2 =  " + (h %= 2));
    }
}
