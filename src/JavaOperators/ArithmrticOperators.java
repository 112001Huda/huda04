package JavaOperators;

public class ArithmrticOperators {
    public static void main(String[] args) {
        int a = 20 ;
        int b = 3 ;
        System.out.println("Addition of 2 numbers are 'a + b ' = "+ (a + b ));
        System.out.println(" Substraction of 2 numbers are 'a - b ' = "+ (a - b ));
        System.out.println("Multiplication of 2 numbers are 'a * b ' = "+ (a * b ));
        System.out.println("Division of 2 numbers are 'a / b ' = "+ (a / b ));
        System.out.println("Modulo of 2 numbers are 'a % b ' = "+ (a % b ));
    }
}
