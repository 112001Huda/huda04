package JavaOperators;

public class UnaryOpratiors {
    public static void main(String[] args) {
        int a = 10 ;
        int b = 11 ;
        System.out.println("increment operator is used to increase value by 1 (+ value by 1)");
        System.out.println("Pre-increment :" + (++a));
        System.out.println("Post-increment : " + (a++));
        System.out.println(" Decrement Operator is used to Decrease value by 1 (- value by 1)");
        System.out.println("Pre-decrement : " + (--b));
        System.out.println("Post-Decrement : " + (b--));
    }
}
